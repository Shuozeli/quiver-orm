fn gen_filter_helpers(w: &mut CodeWriter, m: &ModelDef, schema: &Schema) {
    w.block("pub mod filter", |w| {
        w.line("use super::*;");
        w.blank();

        for f in &m.fields {
            let field_name = &f.name;
            let fn_prefix = to_snake(field_name);
            let filter_type = classify_filter_type(&f.type_expr, schema);

            // eq
            w.block(
                &format!("pub fn {fn_prefix}_eq(value: impl Into<Value>) -> Filter"),
                |w| {
                    w.line(&format!("Filter::eq(\"{field_name}\", value)"));
                },
            );
            w.blank();

            // neq
            w.block(
                &format!("pub fn {fn_prefix}_neq(value: impl Into<Value>) -> Filter"),
                |w| {
                    w.line(&format!("Filter::neq(\"{field_name}\", value)"));
                },
            );
            w.blank();

            // is_null (for nullable fields)
            if f.type_expr.nullable {
                w.block(
                    &format!("pub fn {fn_prefix}_is_null() -> Filter"),
                    |w| {
                        w.line(&format!("Filter::is_null(\"{field_name}\")"));
                    },
                );
                w.blank();

                w.block(
                    &format!("pub fn {fn_prefix}_is_not_null() -> Filter"),
                    |w| {
                        w.line(&format!("Filter::is_not_null(\"{field_name}\")"));
                    },
                );
                w.blank();
            }

            // in
            w.block(
                &format!("pub fn {fn_prefix}_in(values: Vec<Value>) -> Filter"),
                |w| {
                    w.line(&format!("Filter::is_in(\"{field_name}\", values)"));
                },
            );
            w.blank();

            match filter_type {
                FilterType::Numeric => {
                    for (suffix, method) in [("gt", "gt"), ("gte", "gte"), ("lt", "lt"), ("lte", "lte")] {
                        w.block(
                            &format!("pub fn {fn_prefix}_{suffix}(value: impl Into<Value>) -> Filter"),
                            |w| {
                                w.line(&format!("Filter::{method}(\"{field_name}\", value)"));
                            },
                        );
                        w.blank();
                    }

                    // between
                    w.block(
                        &format!("pub fn {fn_prefix}_between(low: impl Into<Value>, high: impl Into<Value>) -> Filter"),
                        |w| {
                            w.line(&format!("Filter::between(\"{field_name}\", low, high)"));
                        },
                    );
                    w.blank();
                }
                FilterType::String => {
                    // like
                    w.block(
                        &format!("pub fn {fn_prefix}_like(pattern: impl Into<Value>) -> Filter"),
                        |w| {
                            w.line(&format!("Filter::like(\"{field_name}\", pattern)"));
                        },
                    );
                    w.blank();

                    // contains (convenience for %value%)
                    w.block(
                        &format!("pub fn {fn_prefix}_contains(value: &str) -> Filter"),
                        |w| {
                            w.line(&format!("Filter::like(\"{field_name}\", format!(\"%{{}}%\", value))"));
                        },
                    );
                    w.blank();

                    // starts_with
                    w.block(
                        &format!("pub fn {fn_prefix}_starts_with(value: &str) -> Filter"),
                        |w| {
                            w.line(&format!("Filter::like(\"{field_name}\", format!(\"{{}}%\", value))"));
                        },
                    );
                    w.blank();

                    // gt/gte/lt/lte for ordering comparisons
                    for (suffix, method) in [("gt", "gt"), ("gte", "gte"), ("lt", "lt"), ("lte", "lte")] {
                        w.block(
                            &format!("pub fn {fn_prefix}_{suffix}(value: impl Into<Value>) -> Filter"),
                            |w| {
                                w.line(&format!("Filter::{method}(\"{field_name}\", value)"));
                            },
                        );
                        w.blank();
                    }
                }
                FilterType::Boolean => {
                    // is_true / is_false
                    w.block(
                        &format!("pub fn {fn_prefix}_is_true() -> Filter"),
                        |w| {
                            w.line(&format!("Filter::eq(\"{field_name}\", true)"));
                        },
                    );
                    w.blank();

                    w.block(
                        &format!("pub fn {fn_prefix}_is_false() -> Filter"),
                        |w| {
                            w.line(&format!("Filter::eq(\"{field_name}\", false)"));
                        },
                    );
                    w.blank();
                }
            }
        }
    });
    w.blank();
}

fn gen_order_helpers(w: &mut CodeWriter, m: &ModelDef) {
    w.block("pub mod order", |w| {
        w.line("use super::*;");
        w.blank();

        for f in &m.fields {
            // Skip collection types -- can't ORDER BY a list
            if is_collection_type(&f.type_expr) {
                continue;
            }

            let fn_name = to_snake(&f.name);
            let field_name = &f.name;

            w.block(
                &format!("pub fn {fn_name}_asc() -> (&'static str, Order)"),
                |w| {
                    w.line(&format!("(\"{field_name}\", Order::Asc)"));
                },
            );
            w.blank();

            w.block(
                &format!("pub fn {fn_name}_desc() -> (&'static str, Order)"),
                |w| {
                    w.line(&format!("(\"{field_name}\", Order::Desc)"));
                },
            );
            w.blank();
        }
    });
    w.blank();
}

fn gen_relation_defs(w: &mut CodeWriter, m: &ModelDef, schema: &Schema) {
    // Collect relations:
    // ManyToOne: from @@fk on this model (this model holds the FK)
    // OneToMany: from @@fk on other models pointing to this model
    struct RelInfo {
        name: String,
        from_model: String,
        to_model: String,
        fields: Vec<String>,
        references: Vec<String>,
        relation_type: &'static str,
    }

    let mut rels: Vec<RelInfo> = Vec::new();

    // ManyToOne: this model has @@fk pointing to another model
    for attr in &m.attributes {
        if let ModelAttribute::ForeignKey {
            fields,
            references_model,
            references_columns,
            ..
        } = attr
        {
            let fn_name = to_snake(references_model);
            rels.push(RelInfo {
                name: fn_name,
                from_model: m.name.clone(),
                to_model: references_model.clone(),
                fields: fields.clone(),
                references: references_columns.clone(),
                relation_type: "RelationType::ManyToOne",
            });
        }
    }

    // OneToMany: other models have @@fk pointing to this model
    for other in &schema.models {
        if other.name == m.name {
            continue;
        }
        for attr in &other.attributes {
            if let ModelAttribute::ForeignKey {
                fields,
                references_model,
                references_columns,
                ..
            } = attr
            {
                if references_model == &m.name {
                    let fn_name = format!("{}s", to_snake(&other.name));
                    rels.push(RelInfo {
                        name: fn_name,
                        from_model: m.name.clone(),
                        to_model: other.name.clone(),
                        fields: references_columns.clone(),
                        references: fields.clone(),
                        relation_type: "RelationType::OneToMany",
                    });
                }
            }
        }
    }

    if rels.is_empty() {
        return;
    }

    w.block("pub mod relations", |w| {
        w.line("use super::*;");
        w.blank();

        for rel in &rels {
            w.block(
                &format!("pub fn {name}() -> RelationDef", name = rel.name),
                |w| {
                    w.block("RelationDef", |w| {
                        w.line(&format!("name: \"{name}\".to_string(),", name = rel.name));
                        w.line(&format!("from_model: \"{from_model}\".to_string(),", from_model = rel.from_model));
                        w.line(&format!("to_model: \"{to_model}\".to_string(),", to_model = rel.to_model));
                        w.line(&format!(
                            "fields: vec![{}],",
                            rel.fields
                                .iter()
                                .map(|f| format!("\"{f}\".to_string()"))
                                .collect::<Vec<_>>()
                                .join(", ")
                        ));
                        w.line(&format!(
                            "references: vec![{}],",
                            rel.references
                                .iter()
                                .map(|r| format!("\"{r}\".to_string()"))
                                .collect::<Vec<_>>()
                                .join(", ")
                        ));
                        w.line(&format!("relation_type: {},", rel.relation_type));
                    });
                },
            );
            w.blank();

            // Include helper
            w.block(
                &format!("pub fn include_{name}() -> Include", name = rel.name),
                |w| {
                    w.line(&format!("Include::new({name}())", name = rel.name));
                },
            );
            w.blank();
        }
    });
    w.blank();
}

fn gen_create_data(w: &mut CodeWriter, m: &ModelDef, schema: &Schema) {
    let table_name = m.table_name().to_string();

    w.line("#[derive(Debug, Clone)]");
    w.block("pub struct CreateData", |w| {
        for f in &m.fields {
            if is_auto_field(f) {
                continue;
            }
            let rust_type = type_to_value_input(&f.type_expr, schema);
            let field_name = to_snake(&f.name);
            if has_default(f) || f.type_expr.nullable {
                w.line(&format!("pub {field_name}: Option<{rust_type}>,"));
            } else {
                w.line(&format!("pub {field_name}: {rust_type},"));
            }
        }
    });
    w.blank();

    // impl to_query
    w.block("impl CreateData", |w| {
        w.block("pub fn to_query(&self) -> BuiltQuery", |w| {
            w.line(&format!("let mut builder = Query::table(\"{table_name}\").create();"));
            for f in &m.fields {
                if is_auto_field(f) {
                    continue;
                }
                let field_snake = to_snake(&f.name);
                let field_name = &f.name;
                if has_default(f) || f.type_expr.nullable {
                    w.block(
                        &format!("if let Some(ref v) = self.{field_snake}"),
                        |w| {
                            w.line(&format!("builder = builder.set(\"{field_name}\", v.clone());"));
                        },
                    );
                } else {
                    w.line(&format!("builder = builder.set(\"{field_name}\", self.{field_snake}.clone());"));
                }
            }
            w.line("builder.build()");
        });
    });
    w.blank();
}

fn gen_update_data(w: &mut CodeWriter, m: &ModelDef, schema: &Schema) {
    let table_name = m.table_name().to_string();

    w.line("#[derive(Debug, Clone, Default)]");
    w.block("pub struct UpdateData", |w| {
        for f in &m.fields {
            if is_auto_field(f) {
                continue;
            }
            let rust_type = type_to_value_input(&f.type_expr, schema);
            let field_name = to_snake(&f.name);
            w.line(&format!("pub {field_name}: Option<{rust_type}>,"));
        }
    });
    w.blank();

    // impl to_query
    w.block("impl UpdateData", |w| {
        w.block(
            "pub fn to_query(&self, filter: Filter) -> BuiltQuery",
            |w| {
                w.line(&format!("let mut builder = Query::table(\"{table_name}\").update().filter(filter);"));
                for f in &m.fields {
                    if is_auto_field(f) {
                        continue;
                    }
                    let field_snake = to_snake(&f.name);
                    let field_name = &f.name;
                    w.block(
                        &format!("if let Some(ref v) = self.{field_snake}"),
                        |w| {
                            w.line(&format!("builder = builder.set(\"{field_name}\", v.clone());"));
                        },
                    );
                }
                w.line("builder.build()");
            },
        );
    });
    w.blank();
}

enum FilterType {
    Numeric,
    String,
    Boolean,
}

fn classify_filter_type(type_expr: &TypeExpr, schema: &Schema) -> FilterType {
    match &type_expr.base {
        BaseType::Boolean => FilterType::Boolean,
        BaseType::Utf8 | BaseType::LargeUtf8 => FilterType::String,
        BaseType::Named(name) => {
            if schema.enums.iter().any(|e| e.name == *name) {
                FilterType::String // enums stored as text
            } else {
                FilterType::Numeric // FK reference
            }
        }
        BaseType::Decimal128 { .. } | BaseType::Decimal256 { .. } => FilterType::String, // stored as text
        _ => FilterType::Numeric,
    }
}

fn is_collection_type(type_expr: &TypeExpr) -> bool {
    is_collection_base_type(&type_expr.base) || matches!(type_expr.base, BaseType::Struct(_))
}

fn type_to_value_input(type_expr: &TypeExpr, schema: &Schema) -> String {
    // For CreateData/UpdateData, we use Value since that's what the query builder accepts
    match &type_expr.base {
        BaseType::Int8 | BaseType::Int16 | BaseType::Int32 | BaseType::Int64 => "i64".into(),
        BaseType::UInt8 | BaseType::UInt16 | BaseType::UInt32 | BaseType::UInt64 => "u64".into(),
        BaseType::Float16 | BaseType::Float32 | BaseType::Float64 => "f64".into(),
        BaseType::Boolean => "bool".into(),
        BaseType::Utf8
        | BaseType::LargeUtf8
        | BaseType::Decimal128 { .. }
        | BaseType::Decimal256 { .. }
        | BaseType::Date32
        | BaseType::Date64
        | BaseType::Time32 { .. }
        | BaseType::Time64 { .. }
        | BaseType::Timestamp { .. } => "String".into(),
        BaseType::Binary | BaseType::LargeBinary | BaseType::FixedSizeBinary { .. } => {
            "Vec<u8>".into()
        }
        BaseType::Named(name) => {
            if schema.enums.iter().any(|e| e.name == *name) {
                "String".into()
            } else {
                "i64".into() // FK
            }
        }
        BaseType::List(_) | BaseType::LargeList(_) | BaseType::Map { .. } | BaseType::Struct(_) => {
            "String".into()
        } // JSON serialized
    }
}

