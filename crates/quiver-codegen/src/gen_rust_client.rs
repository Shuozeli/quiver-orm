use codegen_core::CodeWriter;
use quiver_error::QuiverError;
use quiver_schema::Schema;
use quiver_schema::ast::*;

use crate::helpers::{
    has_default, is_auto_field, is_collection_base_type, to_screaming_snake, to_snake,
};

/// Generates a type-safe Rust client from a Quiver schema.
///
/// For each model, generates a module with:
/// - Field name constants (`&'static str`)
/// - Query entry points (`find_many()`, `create()`, etc.)
/// - Per-field filter constructors
/// - Relation definitions for eager loading
/// - Typed `CreateData`/`UpdateData` structs
pub struct RustClientGenerator;

impl RustClientGenerator {
    pub fn generate(schema: &Schema) -> Result<String, QuiverError> {
        let mut w = CodeWriter::new();

        w.line("// Generated by quiver-codegen. Do not edit.");
        w.blank();
        w.line("use quiver_driver_core::Value;");
        w.line("use quiver_query::{Query, BuiltQuery, Filter, Order};");
        w.line("use quiver_query::{FindManyBuilder, FindFirstBuilder, CreateBuilder};");
        w.line("use quiver_query::{CreateManyBuilder, UpdateBuilder, DeleteBuilder, UpsertBuilder};");
        w.line("use quiver_query::{AggregateBuilder, RelationDef, RelationType, Include};");
        w.blank();

        for m in &schema.models {
            gen_model_module(&mut w, m, schema);
        }

        Ok(w.finish())
    }
}

fn gen_model_module(w: &mut CodeWriter, m: &ModelDef, schema: &Schema) {
    let mod_name = to_snake(&m.name);
    let table_name = m.table_name().to_string();

    w.block(&format!("pub mod {mod_name}"), |w| {
        w.line("use super::*;");
        w.blank();

        gen_field_constants(w, m);
        gen_query_entry_points(w, &table_name);
        gen_filter_helpers(w, m, schema);
        gen_order_helpers(w, m);
        gen_relation_defs(w, m, schema);
        gen_create_data(w, m, schema);
        gen_update_data(w, m, schema);
    });
    w.blank();
}

fn gen_field_constants(w: &mut CodeWriter, m: &ModelDef) {
    w.block("pub mod fields", |w| {
        for f in &m.fields {
            let const_name = to_screaming_snake(&f.name);
            w.line(&format!("pub const {const_name}: &str = \"{name}\";", name = f.name));
        }
    });
    w.blank();
}

